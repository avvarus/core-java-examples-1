package com.company.training;

public class DoWhileLoop {

    public static void main(String[] args) {


        /**
         *
         * do{
         *
         *     // code block
         * }
         * while(condition)
         *
         */

        int i =0;

        do{
            System.out.println(i);
            i++;
        }while (i<15);


    }
}
